package in.vamsoft.training.general;

/**
 * this class contains maths function
 * 
 * @author vamsoft
 *
 */
public class Math {

  /**.
   * This is add to numbers
   * 
   * @param a.
   * @param b.
   * @return.
   */
  public int sum(int a, int b) {
    return a + b;
  }

  /**.
   * This add to numbers
   * 
   * @param x.
   * @param y.
   * @return.
   */
  public double sum(double x, double y) {
    return x + y;
  }

}
